
package TypeCasting;

public class Premitive {
    public static void main(String[] args){
        
        int x = 10;
        double y = x;
        System.out.println(y);
        byte b = 120;
        int i = b;
        System.out.println(i);
        double d = 109;
        int u = (int) d;
        System.out.println(u);
        
        
    }
    
}
